package sec10;

public class AssignOp {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		
		a += 4; // a = a + 4; => 14
		System.out.println("a = "+ a);
		b -=2; // b = b - 2; => 18
		System.out.println("b = "+ b);
		a *= 3; // a = a * 3; => 42
		System.out.println("a = "+ a);

	}
}
